class StarRectangle{
    public static void main(String[] args)
    {
        int m=5;// rows 
        int n=7; // columns 
        for(int i=0;i<m;i++)
        {
            for(int j=0;j<=n;j++)
            {
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}