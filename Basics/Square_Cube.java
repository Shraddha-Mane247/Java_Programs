import java.util.*;
class Square_Cube
{
    public static void main(String args[])
    {
        int no;
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter a no :");
        no=sc.nextInt();

        System.out.println("Square of :"+no+" is ="+(no*no));
        System.out.println("Square of :"+no+" is ="+(no*no*no));

    }
}